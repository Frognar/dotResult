namespace DotResult;

/// <summary>
/// Defines constant values for different types of failures.
/// </summary>
public static class FailureType
{
    /// <summary>
    /// Represents a fatal failure type.
    /// </summary>
    public const string Fatal = "Fatal";

    /// <summary>
    /// Represents a not found failure type.
    /// </summary>
    public const string NotFound = "NotFound";

    /// <summary>
    /// Represents a validation failure type.
    /// </summary>
    public const string Validation = "Validation";
}
